// 7 - Quais as diferenças entre os tipos por valor e os tipos por referência ?

// Resposta:
/*
Os tipos por valores são armazenados na memória STACK, signifinado que cada variável tem diretamente a cópia do valor armazenado em sí, sendo que estão na memória STACK eles sofrem por "Last in first out"

Os tipos por refêrencia são armazenados na memória heap, o tipo de dado mais comum que é armazenado dessa forma são strings, onde não existe diretamente uma cópia dos valores dentro de sí, mas sim existe uam referência para o contéudo.  

